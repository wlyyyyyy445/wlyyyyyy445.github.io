let currentLang = localStorage.getItem('lang') || 'zh-CN';

function t(key) {
  return (LANG[currentLang] && LANG[currentLang][key]) || (LANG['en'][key]) || key;
}

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('lang', lang);
  applyLang();
}

function applyLang() {
  document.title = t('title');
  // Update all data-i18n elements
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var key = el.getAttribute('data-i18n');
    if (key) {
      if (el.tagName === 'INPUT' && el.type === 'placeholder') {
        el.placeholder = t(key);
      } else {
        el.textContent = t(key);
      }
    }
  });
  // Update tab buttons
  updateLangUI();
  // Refresh history if visible
  if (document.getElementById('tab-history') && document.getElementById('tab-history').classList.contains('active')) {
    renderSessions();
  }
}

function updateLangUI() {
  // Update recording button
  var btn = document.getElementById('btn-recording');
  if (btn && !isRecording) btn.textContent = '🔴 ' + t('startRecord');
  if (btn && isRecording) btn.textContent = '⏹ ' + t('stopRecord');
  // Update filter buttons
  document.querySelectorAll('.filter-btn').forEach(function(b) {
    var type = b.getAttribute('onclick') || '';
    if (type.includes("'all')")) b.textContent = '📂 ' + t('allCat');
    if (type.includes("'Web')")) b.textContent = '🌐 ' + t('web');
    if (type.includes("'Desktop')")) b.textContent = '💻 ' + t('desktop');
    if (type.includes("'Flutter')")) b.textContent = '📱 ' + t('phone');
  });
}

// Init on load
document.addEventListener('DOMContentLoaded', function() {
  applyLang();
});

// Set initial language selector value
document.addEventListener('DOMContentLoaded', function() {
  var sel = document.getElementById('lang-select');
  if (sel) sel.value = currentLang;
});
